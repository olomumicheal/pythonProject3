"""
Data Types in python programming languange
1. string data type 
2. int data type
3. float data type
4. boolean data type
5. list data type
5. tuple data type
6. set data type
7. dictionary data type
"""

#1. String data type
firstName = "Micheal"
lastName = "Olomu"

print(firstName)
print(lastName)

#string concatenation
fullName = firstName + " " + lastName + " is 25 years old"
print(fullName)

#2. int data type
x = 200
y = 400
print(x)
print(y)

#3. float data type
a = 2.2
b = 0.7688
print(a)
print(b)

#4. True or False data
isMarried = True
isNotMarried = False
print(isMarried)
print(isNotMarried)
print(type(isMarried))


#5. list data type
myList = ["fruits", "universities", "Ministries"]
print(myList)
print(type(myList))

#6. tuple data type
myTuple = ("Great", "Churches", "Mosque")
print(myTuple)
print(type(myTuple))

#7. set data type
mySet = {"Mathematics", "Statistics", "Arithmetics", 40, False}
print(mySet)
print(type(mySet))

#8. Dictionary data type
myDictionary = {
    "firstName": "Samuel",
    "lastName": "James",
    "age": 26,
    "isMarried": False,
    "myFavGame": ["Football", "Volleyball", "Basketball"],
}

print(myDictionary)
print(type(myDictionary))